				<h2>Malindu</h2>
				<h3>Bulletje, geboren mei 2014</h3>
				<p>Malindu was nog maar een half jaar oud toen hij werd gevonden op een eilandje te midden van het Mau Ara reservoir in het Udawalawe National Park. Hoewel het zeer zeldzaam is dat een krokodil een olifant aanvalt, was Malindu het slachtoffer van een aanval door een krokodil geworden. De kleine Malindu had hieraan veel verwondingen overgehouden. Er werd gezocht naar de moeder van het kleine bulletje. Ondanks dat er voetafdrukken van olifanten rondom de waterkant stonden, was er geen kudde in de buurt te vinden. Malindu werd naar het ETH gebracht, waar zijn wonden werden behandeld. Deze zijn inmiddels genezen, er zijn alleen nog enkele littekens over. Malindu is door de andere weesolifantjes liefdevol opgevangen. Malindu is een verlegen olifantje dat het liefst in het midden van de groep verblijft, omringt door de grotere olifantjes die hem beschermen.</p>
